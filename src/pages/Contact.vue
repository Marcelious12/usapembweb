<script>
export default {
  name: 'Contact',
  data() {
    return {
      form: {
        name: '',
        email: '',
        subject: '',
        message: ''
      },
      errors: {
        name: '',
        email: '',
        message: ''
      },
      isSubmitting: false,
      formSubmitted: false
    }
  },
  computed: {
    messageLength() {
      return this.form.message.length
    },
    isFormValid() {
      return this.form.name.trim().length >= 3 &&
             this.isValidEmail(this.form.email) &&
             this.form.message.trim().length >= 10
    }
  },
  mounted() {
    this.initScrollReveal()
  },
  methods: {
    isValidEmail(email) {
      const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/
      return emailRegex.test(email)
    },
    validateField(field) {
      this.errors[field] = ''

      switch (field) {
        case 'name':
          if (!this.form.name.trim()) {
            this.errors.name = 'Nama lengkap wajib diisi'
          } else if (this.form.name.trim().length < 3) {
            this.errors.name = 'Nama minimal 3 karakter'
          }
          break
        case 'email':
          if (!this.form.email.trim()) {
            this.errors.email = 'Email wajib diisi'
          } else if (!this.isValidEmail(this.form.email)) {
            this.errors.email = 'Format email tidak valid'
          }
          break
        case 'message':
          if (!this.form.message.trim()) {
            this.errors.message = 'Pesan wajib diisi'
          } else if (this.form.message.trim().length < 10) {
            this.errors.message = 'Pesan minimal 10 karakter'
          } else if (this.form.message.trim().length > 500) {
            this.errors.message = 'Pesan maksimal 500 karakter'
          }
          break
      }
    },
    clearError(field) {
      this.errors[field] = ''
    },
    validateForm() {
      this.validateField('name')
      this.validateField('email')
      this.validateField('message')
      return !this.errors.name && !this.errors.email && !this.errors.message
    },
    submitForm() {
      if (!this.validateForm()) {
        return
      }

      this.isSubmitting = true

      // Simulate API call
      setTimeout(() => {
        this.isSubmitting = false
        this.formSubmitted = true
      }, 1500)
    },
    resetForm() {
      this.form = {
        name: '',
        email: '',
        subject: '',
        message: ''
      }
      this.errors = {
        name: '',
        email: '',
        message: ''
      }
      this.formSubmitted = false
    },
    initScrollReveal() {
      const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
          if (entry.isIntersecting) {
            entry.target.classList.add('active')
          }
        })
      }, { threshold: 0.1 })

      document.querySelectorAll('.reveal').forEach(el => observer.observe(el))
    }
  }
}
</script>

